package day_02_variables_data_types;

public class J01_WholeNumbers {


    public static void main(String[] args) {

//DataType variableName=value;

        byte maxByteValue=127;
        //   enBüyükShortDeğeri
        short maxShortValue=32_767;
        //1.000.000

        int maxIntValue=2_147_483_647;

        long maxLongValue=9_223_372_036_854_775_807L;


        System.out.println(maxByteValue);
        System.out.println(maxShortValue);
        System.out.println(maxIntValue);
        System.out.println(maxLongValue);




    }





}
