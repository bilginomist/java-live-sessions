package day_04_arithmetic_operators;

public class J08_Calculator {


    public static void main(String[] args) {

        int num1=25;

        int num2=27;

        System.out.println(num1 + " ve " + num2 +" sayısının toplamı: " + (num1+num2));
        System.out.println(num1 + " ve " + num2 +" sayısının farkı: " + (num1-num2));
        System.out.println(num1 + " ve " + num2 +" sayısının çarpımı: " + (num1*num2));






    }
}
