package day_05_assignment_unary;

public class J01_MixedOperators {


    public static void main(String[] args) {

        byte num1=10;
        short num2=32650;


        short sum= (short) (num1+num2);
        System.out.println(sum);


        short snum3=10;
        float fnum4=12.0F;

        double result=snum3*fnum4;
        System.out.println(result);
        System.out.println(snum3*fnum4);



    }
}
