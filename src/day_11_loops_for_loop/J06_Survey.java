package day_11_loops_for_loop;

public class J06_Survey {


    public static void main(String[] args) {
        /*

        Adınız?
        Soyadınız?
        Mesleğiniz?
        Yaş?
        Hangi Takımlısınız?
         */

        for (int i = 1; i <= 20;i++) {
            System.out.println(i + ". kişi");
            System.out.println("Adınız?");
            System.out.println("Soyadınız?");
            System.out.println("Mesleğiniz?");
            System.out.println("Yaşınız?");
            System.out.println("Hangi takımlsınız?");
            System.out.println();
        }






    }
}
